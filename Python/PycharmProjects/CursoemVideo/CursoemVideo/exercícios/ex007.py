n1 = float(input('Digite sua primeira nota de uma matéria: '))
n2 = float(input('Digite sua segunda nota de uma matéria: '))
n3 = float(input('Digite sua terceira nota de uma matéria: '))
m = (n1 + n2 + n3)/3
print('A sua média das notas {:.1f}, {:.1f} e {:.1f}, foi de: {:.1f}'.format(n1, n2, n3, m))

